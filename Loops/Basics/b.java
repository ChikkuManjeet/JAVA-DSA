// print previous number for that number that is given from user
import java.util.*;
public class b {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter The maxmum limit of number");
        int n = sc.nextInt();
        int a = 0;
        while (a<=n) {
            System.out.print(a + " ");
            a++;
    }
    sc.close();
 }
} 
